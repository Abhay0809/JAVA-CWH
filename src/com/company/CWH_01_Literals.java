package com.company;

public class CWH_01_Literals {
    public static void main(String[] args) {
        byte age = 20;
        int age2 = 56;
        short age3 = 55;
        long age4 = 3895830893L;
        char ch = 'A';
        float f1 = 5.6f;
        double d1 = 4.66D; //BY DEFAULT DECIMAL NUMBER ARE DOUBLE
        boolean a = true;
        System.out.println(age); //LN CHANGES THE LINE
        String name = "Abhay";
        System.out.println(name);
    }
}
